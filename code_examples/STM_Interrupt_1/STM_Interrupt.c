/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define ISR_PRIORITY_STM_INT0   40              /* Priority for interrupt ISR */
#define TIMER_INT_TIME          TimeConst_100ms /* Time between interrupts */

/* In KIT_AURIX_TC297_TFT (AURIX TFT board) 4 LEDs are available:
 *  - IfxPort_P13_0
 *  - IfxPort_P13_1
 *  - IfxPort_P13_2
 *  - IfxPort_P13_3
 */
#define LED     &MODULE_P13,0
#define STM     &MODULE_STM0 /* STM0 will be used in this example */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxStm_CompareConfig compareConfig;

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* Macro to define Interrupt Service Routine.
 * This macro makes following definitions:
 * 1) Define linker section as .intvec_tc<vector number>_<interrupt priority>.
 * 2) define compiler specific attribute for the interrupt functions.
 * 3) define the Interrupt service routine as Isr function.
 *
 * IFX_INTERRUPT(isr, vectabNum, priority)
 *  - isr: Name of the Isr function.
 *  - vectabNum: Vector table number.
 *  - priority: Interrupt priority. Refer Usage of Interrupt Macro for more details.
 */
IFX_INTERRUPT(STM_Int0_Handler, 0, ISR_PRIORITY_STM_INT0);

void STM_Int0_Handler (void)
{
    IfxStm_increaseCompare(STM, compareConfig.comparator, TIMER_INT_TIME); /* Update the compare register value that will trigger the next interrupt */
    IfxPort_setPinState(LED, IfxPort_State_toggled); /* Toggle LED */
}

/*
 * Function to initialize STM and LED
 */
void initPeripherals (void)
{
    initTime(); /* Initialize time constants */
    IfxPort_setPinMode(LED, IfxPort_Mode_outputPushPullGeneral); /* Initialize global variables for LED; */

    /* -------------------- Initialize STM -----------------*/
    IfxStm_initCompareConfig(&compareConfig); /* Initialize compare register configuration with default values */
    compareConfig.triggerPriority = ISR_PRIORITY_STM_INT0; /* Set the priority */
    compareConfig.typeOfService = IfxSrc_Tos_cpu0; /* Set the type of service for STM */
    compareConfig.ticks = TIMER_INT_TIME; /* Set number of ticks after which the timer will trigger an interrupt for the first time */
    IfxStm_initCompare(STM, &compareConfig); /* Initialize STM compare register */
    /* -----------------------------------------------------*/
}
