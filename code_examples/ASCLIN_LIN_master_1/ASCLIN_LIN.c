/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
static IfxAsclin_Lin g_linMaster;

#define SERIAL_BAUDRATE 19200           /* Set baudrate */
#define SIZE 12                         /* Size of the message */
#define ID_BYTE 0x80                    /* Set the id byte */

#define RX_PIN IfxAsclin1_RXB_P15_5_IN  /* Set RX pin */
#define TX_PIN IfxAsclin1_TX_P15_5_OUT  /* Set TX pin */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* This function will initialize the ASCLIN LIN module in master mode */
void init_ASCLIN_LIN_master(void)
{
    /* Create module config */
    IfxAsclin_Lin_Config linMasterConfig;
    IfxAsclin_Lin_initModuleConfig(&linMasterConfig, &MODULE_ASCLIN1);

    /* Set the lin mode of operation */
    linMasterConfig.linMode = IfxAsclin_LinMode_master;

    /* Set the desired baudrate */
    linMasterConfig.brg.baudrate = SERIAL_BAUDRATE;

    /* Pin configuration */
    const IfxAsclin_Lin_Pins pins =
    {
        &RX_PIN,   IfxPort_InputMode_pullUp,    /* Rx pin */
        &TX_PIN,   IfxPort_OutputMode_pushPull, /* Tx pin */
        IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    linMasterConfig.pins = &pins;

    /* Initialize module */
    IfxAsclin_Lin_initModule(&g_linMaster, &linMasterConfig);
}

/* This function will send the string "Hello World!" */
void send_ASCLIN_LIN_message(void)
{
    uint8 txId = ID_BYTE; /* ID which gets transmitted within the header */
    uint8 txData[SIZE] = {'H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd', '!'}; /* frame to send */

    /* Sending part */
    /* The LIN driver provides three master elementary transfers: transmission of header/message and reception of message.
     * After the transmission of the header by the master, either the master itself or an slave will start with the transmission of the message.
     */
    IfxAsclin_Lin_sendHeader(&g_linMaster, &txId); /* Send header for sending message after header */

    if (g_linMaster.acknowledgmentFlags.txHeaderEnd == 1) /* If the header is been transmitted successfully */
    {
        IfxAsclin_Lin_sendResponse(&g_linMaster, txData, SIZE); /* Send message */
    }
}
