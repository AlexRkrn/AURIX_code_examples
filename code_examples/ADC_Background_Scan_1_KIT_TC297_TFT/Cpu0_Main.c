#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "ADC_Background_Scan.h"

IfxCpu_syncEvent g_cpuSyncEvent = 0;

int core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);
    
    /* Initialize the VADC module and start the background scan */
    initADC();

    /* Variables for showing the conversion results of each channel */
    volatile uint16 chn0Measurement = 0;
    volatile uint16 chn1Measurement = 0;
    volatile uint16 chn2Measurement = 0;
    volatile uint16 chn3Measurement = 0;

    /* Continuously update the conversion results of each channel */
    while(1)
    {
        chn0Measurement = readADCValue(CHN_0);
        chn1Measurement = readADCValue(CHN_1);
        chn2Measurement = readADCValue(CHN_2);
        chn3Measurement = readADCValue(CHN_3);
    }
    return (1);
}
